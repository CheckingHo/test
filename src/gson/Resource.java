package gson;

public class Resource {

	private RtdModule rtdModule ;
	
	private TestCard testCard ;
	
	private String systemId ;

	public RtdModule getRtdModule() {
		return rtdModule;
	}

	public void setRtdModule(RtdModule rtdModule) {
		this.rtdModule = rtdModule;
	}

	public TestCard getTestCard() {
		return testCard;
	}

	public void setTestCard(TestCard testCard) {
		this.testCard = testCard;
	}

	public String getSystemId() {
		return systemId;
	}

	public void setSystemId(String systemId) {
		this.systemId = systemId;
	}
	
	
}
